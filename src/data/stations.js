// Build fast lookup maps
export const STATION_BY_CODE = STATIONS.reduce((acc, s) => {
  acc[s.code] = s.name;
  return acc;
}, {});

export const CODE_BY_NAME = STATIONS.reduce((acc, s) => {
  acc[s.name.toLowerCase()] = s.code;
  return acc;
}, {});

// Search function — matches code or name (case-insensitive, partial match)
export function searchStations(query, limit = 8) {
  if (!query || query.length < 1) return [];
  const q = query.toLowerCase().trim();

  const codeMatches = [];
  const nameStartMatches = [];
  const nameContainMatches = [];

  for (const s of STATIONS) {
    const codeLower = s.code.toLowerCase();
    const nameLower = s.name.toLowerCase();

    if (codeLower === q) {
      codeMatches.unshift(s); // exact code match first
    } else if (codeLower.startsWith(q)) {
      codeMatches.push(s);
    } else if (nameLower.startsWith(q)) {
      nameStartMatches.push(s);
    } else if (nameLower.includes(q)) {
      nameContainMatches.push(s);
    }
  }

  return [...codeMatches, ...nameStartMatches, ...nameContainMatches].slice(0, limit);
}

export function resolveStationCode(input) {
  if (!input) return '';
  const q = input.trim();
  // Already a code
  const upper = q.toUpperCase();
  if (STATION_BY_CODE[upper]) return upper;
  // Match by name
  const lower = q.toLowerCase();
  if (CODE_BY_NAME[lower]) return CODE_BY_NAME[lower];
  // Fuzzy — find best partial match
  const matches = searchStations(q, 1);
  return matches[0]?.code || upper;
}