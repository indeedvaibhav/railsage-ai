// Search trains where route contains BOTH from and to, in correct order
export function findTrainsBetween(fromCode, toCode) {
  if (!fromCode || !toCode) return [];

  return TRAIN_ROUTES.filter(train => {
    const fromIdx = train.stops.indexOf(fromCode);
    const toIdx = train.stops.indexOf(toCode);
    // Both stations must exist on this route, and from must come before to
    return fromIdx !== -1 && toIdx !== -1 && fromIdx < toIdx;
  }).map(train => ({
    train_number: train.trainNumber,
    train_name: train.trainName,
    source: train.source,
    destination: train.destination,
    // Extra info for journey results — actual boarding/deboarding points
    boardingStation: fromCode,
    deboardingStation: toCode,
  }));
}